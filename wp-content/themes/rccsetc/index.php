    <p>The School of Engineering Technology &amp; Computing (SETC) at <a href="http://www.rccit.ca/" target="_blank">RCC Institute of Technology</a> offers a fast and effective path to a rewarding technology career. With small class sizes (an average of 20 students per class), strong relationships with more than 3000 employers and a dedicated, accessible <a href="http://www.rccsetc.ca/About/Our-People/Faculty">faculty</a> who get to know students on a personal level, SETC offer the support, knowledge base and hands-on training essential to prepare for today’s job market. SETC’s programs give students great flexibility in balancing program length, level of skills acquired, and career options.</p>
<p>For more than <a href="http://www.rccsetc.ca/About/History">80 years</a>, SETC has supplied the technology sector with highly skilled graduates. Beginning in 1927 as a training facility for the radio and communications sector, the school quickly became a leader in electronics, micro-processing and computer-based systems education. Since its inception, SETC (formerly called the RCC Institute of Technology) has also been a leading proponent of <a href="http://www.rccsetc.ca/About/Accreditations">certification and standardization</a> within the field of technology.</p>
<h2>Today, SETC offers the following programs:</h2>
<p><a href="http://www.rccsetc.ca/Programs/Bachelor-of-Technology">Bachelor of Technology (Electronics Engineering Technology)</a> (2 years, 9 months)<br>
<a href="http://www.rccsetc.ca/Programs/Electronics-Engineering-Technology-Diploma">Electronics Engineering Technology diploma</a>&nbsp;(2 years)<br>
<a href="http://www.rccsetc.ca/Programs/Electronics-Engineering-Technician-Diploma">Electronics Engineering Technician diploma</a>&nbsp;(1 years)<br>
<a href="http://www.rccsetc.ca/Programs/Bachelor-of-Business-Information-Systems"> Bachelor of Business Information Systems</a></p>
<h2>SETC’s core principles are:</h2>
<p><strong>Consistency and Focus</strong><br>
All programs are career-focused and application-oriented.</p>
<p><strong>Real-World Knowledge</strong><br>
Program Advisory Committees, which meet regularly and are informed by industry feedback, ensure SETC curricula align with actual work sector expectations and requirements.</p>
<p><strong>Skills-Based Education</strong><br>
Class sizes are kept small to ensure optimum instructor-to-student ratios. Faculty plan and deliver courses to emphasize hands-on projects that engage students and facilitate learning.</p>
